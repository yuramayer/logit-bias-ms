from __future__ import annotations

"""Generate a larger fixed prompt set for the scaled defense experiment."""

import json
from pathlib import Path


TOPICS = [
    "строгой формализации метрик в управляемой генерации",
    "позиционном применении logit bias",
    "дискурсивных маркерах как наблюдаемом слое тона",
    "сохранении смысла при управлении текстом",
    "языковой цене вмешательства в декодирование",
    "токенизации и многотокенных маркерах",
    "сегментной аппроксимации позиционного вмешательства",
    "различии между prompt engineering и decoding-time control",
    "ограничениях словарного подхода к анализу дискурса",
    "сравнении раннего и позднего вмешательства",
    "роли средней части ответа в разворачивании аргумента",
    "устойчивости результатов между повторными генерациями",
    "контроле стохастического шума при низкой температуре",
    "интерпретации отрицательного logit bias",
    "различии между маркерным сдвигом и деградацией текста",
    "проверке гипотез на prompt-level уровне",
    "логико-структурных связках в академическом ответе",
    "эпистемическом хеджировании в сгенерированном тексте",
    "переносимости результатов между моделями одного семейства",
    "практическом использовании позиционного управления генерацией",
]

TASKS = [
    "Сформулируй краткое академическое объяснение",
    "Напиши аналитический фрагмент",
    "Подготовь методологический комментарий",
    "Дай осторожную исследовательскую оценку",
    "Сопоставь два возможных объяснения",
]

REQUIREMENTS = [
    "Укажи два аргумента и короткий вывод",
    "Сначала задай тезис, затем одну оговорку и итог",
    "Выдели одно преимущество и одно ограничение",
    "Покажи, почему результат нельзя читать изолированно",
    "Объясни, какую роль это играет в дизайне эксперимента",
]


def main() -> None:
    prompts = []
    prompt_id = 1
    for topic in TOPICS:
        for task, requirement in zip(TASKS, REQUIREMENTS, strict=True):
            prompts.append(
                {
                    "id": f"s{prompt_id:03d}",
                    "text": (
                        f"{task} о {topic}. {requirement}. "
                        "Пиши в академическом стиле, без списков."
                    ),
                }
            )
            prompt_id += 1

    output_path = Path(__file__).resolve().parents[1] / "data" / "prompts_vkr_scaled_100.json"
    output_path.write_text(
        json.dumps({"prompts": prompts}, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    print(f"wrote {len(prompts)} prompts to {output_path}")


if __name__ == "__main__":
    main()
