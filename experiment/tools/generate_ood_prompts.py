from __future__ import annotations

"""Generate out-of-domain academic prompts for prompt-transfer validation."""

import json
from pathlib import Path


TOPICS = [
    "доступности городского общественного транспорта",
    "влиянии удаленной работы на организационную культуру",
    "оценке качества школьного образования",
    "регулировании цифровых платформ",
    "устойчивом развитии городской среды",
    "прозрачности бюджетных решений",
    "роли профилактики в общественном здравоохранении",
    "этике использования персональных данных",
    "адаптации рынка труда к автоматизации",
    "доверии к научной коммуникации",
    "экологической ответственности бизнеса",
    "цифровом неравенстве между регионами",
    "оценке эффективности социальных программ",
    "управлении рисками в энергетике",
    "качестве пользовательского опыта в госуслугах",
    "медиаграмотности в условиях информационного шума",
    "правовом регулировании новых технологий",
    "психологии принятия решений в неопределенности",
    "использовании статистики в публичной политике",
    "балансе инноваций и институциональной стабильности",
]

TASKS = [
    "Сформулируй краткое академическое объяснение",
    "Напиши аналитический фрагмент",
    "Подготовь методологический комментарий",
    "Дай осторожную исследовательскую оценку",
    "Сопоставь два возможных объяснения",
]

REQUIREMENTS = [
    "Укажи два аргумента и короткий вывод",
    "Сначала задай тезис, затем одну оговорку и итог",
    "Выдели одно преимущество и одно ограничение",
    "Покажи, почему результат нельзя читать изолированно",
    "Объясни, какую роль это играет в анализе проблемы",
]


def main() -> None:
    prompts = []
    prompt_id = 1
    for topic in TOPICS:
        for task, requirement in zip(TASKS, REQUIREMENTS, strict=True):
            prompts.append(
                {
                    "id": f"o{prompt_id:03d}",
                    "text": (
                        f"{task} о {topic}. {requirement}. "
                        "Пиши в академическом стиле, без списков."
                    ),
                }
            )
            prompt_id += 1

    output_path = Path(__file__).resolve().parents[1] / "data" / "prompts_vkr_ood_100.json"
    output_path.write_text(
        json.dumps({"prompts": prompts}, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    print(f"wrote {len(prompts)} prompts to {output_path}")


if __name__ == "__main__":
    main()
